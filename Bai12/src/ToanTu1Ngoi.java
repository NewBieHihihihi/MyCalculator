
public class ToanTu1Ngoi {
	public static void main(String[] args) {
		int a = 10;
		boolean b = false;
		System.out.println("Gia Tri Cua +a: " + a);
		System.out.println("Gia Tri Cua -a: " + (-a));
		System.out.println("Gia Tri Cua !b: " + !b);
		System.out.println("-----------------------");
		System.out.println("Gia Tri Cua A Ban Dau: " + a);
		System.out.println("Gia Tri Cua ++A: " + (++a));
		System.out.println("Gia Tri Cua A++: " + (a++));// thuc hien xong cau len nay se
		System.out.println("Gia Tri Cua A : " + a);// xuong dong nay gia tri a tang len 1
		System.out.println("Gia Tri Cua --A: " + (--a));
		System.out.println("Gia Tri Cua A--: " + (a--));
		System.out.println("Gia Tri Cua A : " + a);
	}
}
